#include "fillit.h"

void	ft_print_map(char **map)
{
	int j;
	int i;

	i = 0;
	j = 0;
	while (map[i][0] != '\0')
	{
		while (map[i][j] != '\0')
		{
			write(1, &map[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
		j = 0;
	}
}
